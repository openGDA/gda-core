	final class PlotUpdateJob extends Job {
		protected ILineTrace exafsProfile;
		private boolean first = true;

		public PlotUpdateJob(String name) {
			super(name);
			setPriority(Job.INTERACTIVE);
			setUser(true);
			setSystem(true);
		}

		public void createTrace() {
			IntegerDataset x = IntegerDataset.createRange(5);
			IntegerDataset y = IntegerDataset.createRange(5);
			x.setName("Energy (eV)");
			y.setName("\u0394E (eV)");
			List<ITrace> traces = plottingsystem.createPlot1D(x, Arrays.asList(new Dataset[] { y }), null);
			exafsProfile = (ILineTrace) traces.get(0);
			exafsProfile.setTraceColor(Display.getDefault().getSystemColor(SWT.COLOR_BLACK));
		}

		public void plot() {
			cancel();
			schedule();
		}

		@Override
		protected IStatus run(final IProgressMonitor monitor) {
			try {
				Object scanBean = editingBean;
				if (monitor.isCanceled())
					return Status.CANCEL_STATUS;
				if (lastPlottedBean != null && lastPlottedBean.equals(scanBean))
					return Status.CANCEL_STATUS;
				if (monitor.isCanceled())
					return Status.CANCEL_STATUS;
				List<PyObject[]> points = getScanPoints(editingBean);
				double[] y = new double[points.size()];
				double[] x = new double[points.size()];
				double previousEnergy = -1;
				for (int i = 0; i < points.size(); i++) {
					double energy = points.get(i)[0].asDouble();
					x[i] = energy;
					Double delta = 0d;
					if (previousEnergy > -1)
						delta = energy - previousEnergy;
					y[i] = delta;
					previousEnergy = energy;
				}
				y[0] = y[1];
				final DoubleDataset xDataSet = new DoubleDataset(x, x.length);
				final DoubleDataset yDataSet = new DoubleDataset(y, y.length);
				xDataSet.setName("Energy (eV)");
				yDataSet.setName("\u0394E (eV)");
				Display.getDefault().syncExec(new Runnable() {
					@Override
					public void run() {
						// We just replot it.
						exafsProfile.setTraceColor(Display.getDefault().getSystemColor(SWT.COLOR_BLACK));
						exafsProfile.setData(xDataSet, yDataSet);
						if (first) {
							plottingsystem.repaint();
							first = false;
						}
						plottingsystem.autoscaleAxes();
					}
				});
				lastPlottedBean = BeansFactory.deepClone(scanBean);
				return Status.OK_STATUS;
			} catch (Throwable no) {
				lastPlottedBean = null;
				Display.getDefault().syncExec(new Runnable() {
					@Override
					public void run() {
						// We just replot it.
						exafsProfile.setTraceColor(Display.getDefault().getSystemColor(SWT.COLOR_RED));
					}
				});
				return Status.CANCEL_STATUS;
			}
		}

	}
