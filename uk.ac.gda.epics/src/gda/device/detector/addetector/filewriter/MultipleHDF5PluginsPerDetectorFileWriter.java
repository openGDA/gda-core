package gda.device.detector.addetector.filewriter;

import java.util.List;
import java.util.Optional;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import gda.device.detector.EpicsProcessVariableCollection;
import gda.device.detector.nxdata.NXDetectorDataAppender;
import gda.device.detector.nxdata.NXDetectorDataHDF5FileLinkAppender;
import gda.device.detector.nxdata.NXDetectorSerialAppender;

/**
 * A File Writer class supports EPICS Area Detector producing multiple HDF5 files
 */
public class MultipleHDF5PluginsPerDetectorFileWriter extends MultipleImagesPerHDF5FileWriter {

	private static Logger logger = LoggerFactory.getLogger(MultipleHDF5PluginsPerDetectorFileWriter.class);

	@Override
	protected NXDetectorDataAppender createFileLinkAppender() {
		logger.trace("create file link appender to link to multiple HDF5 files generated by EPICS Area Detector plus additional PVs value collection if present");
		NXDetectorDataHDF5FileLinkAppender nxDetectorDataHDF5FileLinkAppender = new NXDetectorDataHDF5FileLinkAppender(expectedFullFileName, getxPixelSize(), getyPixelSize(), getxPixelSizeUnit(), getyPixelSizeUnit(), getName());
		Optional<EpicsProcessVariableCollection> collection = Optional.ofNullable(getPvsToCollectMap());
		if (collection.isPresent()) {
			List<NXDetectorDataAppender> appenders = createAppendersFromCollection(collection.get());
			appenders.add(nxDetectorDataHDF5FileLinkAppender);
			return new NXDetectorSerialAppender(appenders);
		} else {
			return nxDetectorDataHDF5FileLinkAppender;
		}
	}
}
