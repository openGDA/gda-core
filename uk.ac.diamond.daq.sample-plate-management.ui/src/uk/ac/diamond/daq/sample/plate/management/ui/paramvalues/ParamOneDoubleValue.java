package uk.ac.diamond.daq.sample.plate.management.ui.paramvalues;

import gda.factory.FindableBase;

public class ParamOneDoubleValue extends FindableBase implements IParamValue {

	Double value;

	public ParamOneDoubleValue(double value) {
		this.value = value;
	}

	public double getFirstValue() {
		return value;
	}

	public Double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

	@Override
	public String getValueString() {
		return " " + value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((getName() == null) ? 0 : getName().hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ParamOneDoubleValue other = (ParamOneDoubleValue) obj;
		if (getName() == null) {
			if (other.getName() != null)
				return false;
		} else if (!getName().equals(other.getName()))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}