package uk.ac.diamond.daq.mapping.ui.experiment.file;

public class SavedScanMetaData {
	private static final long INVALID_ID = -1;
	private long id;

	private String name;

	public SavedScanMetaData(long id) {
		this(id, "");
	}

	public SavedScanMetaData(String name) {
		this(INVALID_ID, name);
	}

	public SavedScanMetaData(long id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return getName();
	}
}
