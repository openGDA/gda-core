package uk.ac.gda.client.tomo;

import java.util.ArrayList;
import java.util.List;
import java.util.concurrent.Callable;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;
import java.util.concurrent.Future;

public class ReconManager {
	
	public ExecutorService executor = Executors.newCachedThreadPool();
	public List<Future<ReconStatus>> statusList = new ArrayList<Future<ReconStatus>>(); 
	
	public void addRecon(Callable<ReconStatus> reconTask) {
		Future<ReconStatus> submit = executor.submit(reconTask);
		statusList.add(submit);
	}

}
